public class Main {
    public static void main(String[] args) {



        //while(true)
        // synchronized
        System.out.println();// philosopher[i] think

        //synchronized
        System.out.println();// philosopher [i] put the left fork up
        System.out.println();// philosopher[i] put the right fork up
        System.out.println();// philosopher[i] eat

        // synchronized
        System.out.println();// philosopher[i] put the left fork down
        System.out.println();// philosopher[i] put the right fork down

        // synchronized
        System.out.println();// philosopher[i] think again



    }






}
